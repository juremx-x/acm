<script>
    document.addEventListener('DOMContentLoaded', function () {
      // Función para observar si el elemento entra en la pantalla
      const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const element = entry.target;
  
            // Agregar animación basada en el ID del elemento
            if (element.id === 'contenedorAnimado') {
              element.classList.add('animate__animated', 'animate__fadeInLeft');
            } else if (element.id === 'equipoecore') {
              element.classList.add('animate__animated', 'animate__zoomIn');
              element.style.visibility = 'visible'; // Asegura que el elemento sea visible
            }
  
            // Dejar de observar después de la animación
            observer.unobserve(element);
          }
        });
      }, { threshold: 0.5 });  // El elemento debe estar al menos al 50% en pantalla
  
      // Seleccionar los elementos que queremos observar
      const elementoAnimado = document.getElementById('contenedorAnimado');
      const elementoAAnimar = document.getElementById('equipoecore');
  
      // Iniciar la observación
      if (elementoAnimado) observer.observe(elementoAnimado);
      if (elementoAAnimar) observer.observe(elementoAAnimar);
    });
  </script>




